export const SPORT_TYPE_EMOJI_MAP = {
  "Летние": "☀️",
  "Зимние": "❄️",
  "Киберспорт": "🎮",
  "Традиционные": "🎯",
  "Паралимпийские игры": "♿",
  "ГТО": "🏅",
};

export function getSportEmoji(sportName, sportType) {
  if (!sportName) return SPORT_TYPE_EMOJI_MAP[sportType] ?? "🏆";

  // Exact match
  if (SPORT_EMOJI_MAP[sportName]) return SPORT_EMOJI_MAP[sportName];

  // Partial match
  for (const [key, emoji] of Object.entries(SPORT_EMOJI_MAP)) {
    if (sportName.toLowerCase().includes(key.toLowerCase())) return emoji;
  }

  // Type fallback
  return SPORT_TYPE_EMOJI_MAP[sportType] ?? "🏆";
}
